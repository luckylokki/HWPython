x = input('Введите строку: ')
print('Третий символ введенной строки:\n', x[2])
print('Предпоследний символ введной строки:\n', x[-2])
print('Первые пять символов введной строки:\n', x[:5])
print('Вся строка кроме последних двух симовол:\n', x[:-2])
print('Все символы с четными индексами:\n', x[::2])
print('Все символы с нечетными индексами:\n', x[1::2])
print('Все символы в обратном порядке:\n', x[::-1])
print('Все символы строки через один в обратном порядке, начиная с последнего:\n', x[::-2])
print('Длина данной строки:\n', len(x))
